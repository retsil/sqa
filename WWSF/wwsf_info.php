<h2 class="sqa">Worldwide study on split function - WWSSF 08/09</h2>
<h3 class="sqa">Clinical Value of Split Function assessment.</h3>

<div class="sqap">Clinical decision making in patients with hydronephrosis is often based on change in split function. That means if in comparison to initial renal function a decrease occurs in follow-up a surgical intervention might be justified. Diuretic renography with technetium-99m labeled diethylenetriamine-pentaacetic acid (DTPA), L,L-ethylenedicysteine (EC), mercaptoacetyltriglycine (MAG3) or 131-, 123-Iodine-orthoiodohippurate (OIH) provides noninvasive possibility to calculate differential renal function and might be able to differentiate between obstructive from non-obstructive hydronephrosis.</div>

<div class="sqap">The intention of this study was to investigate, in a worldwide study, the reliability of split function in renal scintigraphy.</div>

<div class="sqap">The study was designed as a round-robin test for the renal analysis software in nuclear medicine. Three clinical cases and two phantom studies were sent to participants all over the world and they were asked to report on the split function using their routine software and workflow.</div>

<div class="sqap">The data set included three patients. They were studied on a routine base in our department using the EANM standardized protocol F+20. Following the administration of 99m-Tc MAG3, images were acquired in a 64x64 matrix with a frame rate of 10s/frame for 120 frames. The gamma camera used is equipped with a low-energy high-resolution collimator. The photopeak was selected at 140 keV with a 20% energy window.</div>

